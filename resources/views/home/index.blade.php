<script>
    let carousel  = document.querySelector(".simple-carousel .simple-carousel-inner"),
        item    = carousel.querySelectorAll(".item"),
        img     = carousel.querySelectorAll("img"),
        isDown,
        startX,
        scrollLeft

    carousel.addEventListener('mousedown', (e) => {
        isDown = true
        startX = e.pageX - carousel.offsetLeft
        scrollLeft = carousel.scrollLeft

        gsap.to(item, 0.7, {scale: 0.93, force3d: true}, "none")
        gsap.to(img, 0.9, {scale: 1.19, force3d: true}, "none")
    });

    carousel.addEventListener('mouseup', () => {
        isDown = false

        gsap.to(item, 0.7, {scale: 1, force3d: true}, "none")
        gsap.to(img, 0.8, {scale: 1, force3d: true}, "none")
    });

    carousel.addEventListener('mousemove', (e) => {
        if(!isDown) return
        e.preventDefault()

        const x = e.pageX - carousel.offsetLeft
        const walk = (x - startX) * 2

        gsap.killTweensOf(carousel)
        gsap.to(carousel, {duration: 0.9, scrollTo: {x: scrollLeft - walk, y: 0}, ease: "cubic.out"} )
    });
</script>
